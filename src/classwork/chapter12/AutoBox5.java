package classwork.chapter12;

public class AutoBox5 {

    public static void main(String[] args) {
        Integer iOb = 2;

        switch (iOb) {
            case 1:
                System.out.println("один");
                break;
            case 2:
                System.out.println("два");
                break;
            default:
                System.out.println("ошибка");
        }
    }
}
