package classwork.chapter2.lesson5;

public class ForExample {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.print(i + 1);
            System.out.print(" ");
        }

        System.out.println();

        for (int i = 10; i > 0; i--) {
            System.out.print(i);
            System.out.print(" ");
        }
    }

}
