package classwork.lesson6;

public class CharExample {

    public static void main(String[] args) {
        char a = 89;
        System.out.println(a);

        char b = '?';
        System.out.print(b);

        char c = 10;
        System.out.print(c);

        char d = '\'';
        System.out.println(d);

        System.out.println("\"poxos\"\npetros");
    }

}
